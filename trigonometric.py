import math

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def tan(a):
    return math.tan(a)

def cot(a):
    return 1/math.tan(a)

def sec(a):
    return 1/math.cos(a)

def csc(a):
    return 1/math.sin(a)

def asin(a):
    return math.asin(a)

def acos(a):
    return math.acos(a)

def atan(a):
    return math.atan(a)

def acot(a):
    return math.atan(1/a)

def asec(a):
    return math.acos(1/a)

def acsc(a):
    return math.asin(1/a)